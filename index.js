let firstPassword = document.getElementById("password1");
let secondPassword = document.getElementById("password2");

function generateRandomCharacter() {
  let randomCharacter = Math.floor(Math.random() * passwordCharacter.length);
  return passwordCharacter[randomCharacter];
}

function generatePassword() {
  firstPassword.textContent = "";
  secondPassword.textContent = "";
  for (i = 0; i <= 16; i++) {
    // We need to randomly pick 16 characters, each character for each time this loop runs

    firstPassword.textContent += generateRandomCharacter();
    secondPassword.textContent += generateRandomCharacter();
  }
}
